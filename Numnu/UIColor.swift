import UIKit

extension UIColor {
    
    static func appThemeColor() -> UIColor {
        
        return UIColor(red:234/255, green:33/255, blue:37/255, alpha:1.00)
    }
    
    static func textlightDark() -> UIColor {
        
        return UIColor(red: 119/255, green:123/255, blue:131/255, alpha:1.00)
        
    }
    
    static func appBlackColor() -> UIColor {
        
        return UIColor(red:31/255, green:31/255, blue:31/255, alpha:1.00)
        
    }
    
}
